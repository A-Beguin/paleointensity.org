_dataset_dict = {
    "PI320_db": _data_PI320_db,
    "PI320": _data_PI320,
    "PI320_db_only": _data_PI320_db_only,

}

_dataset_selection_dict = {
    "PI320": _data_PI320_selection,
    "PI320_db": _data_PI320_db_selection,
    "PI320_db_only": _data_PI320_db_only_selection,
}


def get(site, selection=False):
    # select the right dataset dictionary
    dict = _dataset_dict
    if selection:
        dict = _dataset_selection_dict

    # check if the specimen is in the dict
    if (site in dict) == False:
        print("WARNING: the specimen %s is not present in the demo data, asked for a selection: %s" % (site))
        return []
    else:
        return _demo_data_to_real_format_MSP(dict[site])


def _demo_data_to_real_format_MSP(l):
    result = []
    for msrmt in l:
        result.append({
            "site": msrmt[0],
            "specimen": msrmt[1],
            "x": msrmt[2],
            "y": msrmt[3],
            "z": msrmt[4],
            "error": msrmt[5],
            "dec": msrmt[6],
            "inc": msrmt[7],
            "total_m": msrmt[8],
            "msp_temp": msrmt[9],
            "lab_field": msrmt[10],
            "type": msrmt[11],
        })
    return result
